package muku.com;

public class Maths {

	public static void main(String[] args) {
		System.out.println(Math.PI);
		System.out.println(Math.sqrt(3));
		System.out.println(Math.cbrt(8));
		System.out.println(Math.abs(-4));
		System.out.println(Math.pow(2, 3));
		System.out.println(Math.floor(6.999));
		System.out.println(Math.round(6.999));
		System.out.println(Math.ceil(6.00));
		System.out.println(Math.floor(6.00));
		System.out.println(Math.hypot(3, 4));
		System.out.println(Math.max(3, 4.0));
		System.out.println(Math.min(3, 4.0));
		System.out.println(Math.random());
		System.out.println(Math.log10(10));
		System.out.println(Math.log(10));


	}

}
